// Envases 1
const env1Img = document.querySelector('#envases1 img');
env1Img.addEventListener('mouseover', () => {
    env1Img.style.transform = "scale(1.05)";
    env1Img.classList.remove('border-gray-400');
    env1Img.classList.add('border-red-500'); 
    env1Img.classList.add('shadow-2xl');
});

env1Img.addEventListener("mouseout", () => { 
    env1Img.style.transform = "scale(1)";
    env1Img.classList.remove('border-red-500');
    env1Img.classList.add('border-gray-400'); 
    env1Img.classList.remove('shadow-2xl');
});

